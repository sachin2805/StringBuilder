package StringBuilder;

public class CharAt {

	public static void main(String[] args) {

		StringBuilder sb=new StringBuilder("Sachin Powar");
		
		
		System.out.println("Given String :\n"+sb);
		System.out.println("Using CharAt(5) :\n"+sb.charAt(5));
	}

}
/*OUTPUT
Given String :
Sachin Powar
Using CharAt(5) :
n
*/	